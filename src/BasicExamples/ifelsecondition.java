package BasicExamples;

public class ifelsecondition {
    public static void main(String[] args) {
        int age = 19;
        String name = "Gopal";
        if (age > 18) {
            System.out.println("Candidate is major");
            System.out.println(name);
        } else {
            System.out.println("Candidate is minor");
        }
    }
}
